export async function seedVrats() {
  await ensureSanyamDb();
  
  // Add katha_hi column if not exists
  try {
    await dbRun("ALTER TABLE sanyam_vrats ADD COLUMN katha_hi TEXT", []);
    await dbRun("ALTER TABLE sanyam_vrats ADD COLUMN katha_en TEXT", []);
    await dbRun("ALTER TABLE sanyam_vrats ADD COLUMN source TEXT", []);
  } catch { /* columns may already exist */ }

  // Quick check: skip if already seeded with full kathas
  const existing = await dbGet<{c:number}>("SELECT COUNT(*) as c FROM sanyam_vrats WHERE katha_hi IS NOT NULL AND katha_hi != ''", []).catch(()=>null);
  if (existing && existing.c >= JAIN_VRATS.length) return;

  for (const v of JAIN_VRATS) {
    const existing = await dbGet("SELECT id FROM sanyam_vrats WHERE slug=?", [v.slug]);
    if (existing) {
      // Update katha
      await dbRun(
        "UPDATE sanyam_vrats SET katha_hi=?, description_hi=?, procedure_hi=?, rules=?, benefits=?, source=? WHERE slug=?",
        [v.katha_hi, v.description_hi, v.procedure_hi, v.rules||"", v.benefits||"", v.source||"", v.slug]
      );
    } else {
      await dbRun(
        `INSERT INTO sanyam_vrats (name,name_hi,slug,category,emoji,color,difficulty,duration_days,
          jain_month,jain_date,stars_reward,stars_per_day,description,description_hi,procedure_hi,
          rules,benefits,katha_hi,source,is_active,order_index)
         VALUES (?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,1,0)`,
        [v.name,v.name_hi,v.slug,v.category,v.emoji,v.color,v.difficulty,v.duration_days,
         v.jain_month||"",v.jain_date||"",v.stars_reward,v.stars_per_day,
         v.description,v.description_hi,v.procedure_hi||"",
         v.rules||"",v.benefits||"",v.katha_hi||"",v.source||""]
      );
    }
  }
  
  console.log(`[sanyam] ${JAIN_VRATS.length} vrats seeded/updated with kathas`);
}
