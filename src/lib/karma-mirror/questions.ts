import type { KMQuestion } from "@/types";

const FREQUENCY_OPTIONS = [
  { label: "कभी नहीं", value: 1 as const },
  { label: "शायद ही कभी", value: 2 as const },
  { label: "कभी-कभी", value: 3 as const },
  { label: "अक्सर", value: 4 as const },
  { label: "हमेशा", value: 5 as const },
];

function direct(id: string, trait: KMQuestion["trait"], text: string): KMQuestion {
  return { id, trait, type: "direct", text, options: FREQUENCY_OPTIONS };
}

function scenario(
  id: string,
  trait: KMQuestion["trait"],
  text: string,
  labels: [string, string, string, string, string]
): KMQuestion {
  return {
    id,
    trait,
    type: "scenario",
    text,
    options: labels.map((label, i) => ({ label, value: (i + 1) as 1 | 2 | 3 | 4 | 5 })),
  };
}

